let num1 = 3;
let num2 = 2;

let symb = "*";

if (symb === "+") {
    console.log(num1 + num2);
} else if (symb === "-") {
    console.log(num1 - num2);
} else if (symb === "/") {
    console.log(num1 / num2);
} else if (symb === "*") {
    console.log(num1 * num2);
} else {
    console.log("error!")
};